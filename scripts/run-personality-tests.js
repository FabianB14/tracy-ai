#!/usr/bin/env node
// run-personality-tests — send each scenario in personality-tests.json through
// Tracy (single-turn, on the babyresell surface) and produce a results table.
//
// Writes the table to personality-test-results.md and prints it to stdout.
//
// Usage:
//   node scripts/run-personality-tests.js
//   node scripts/run-personality-tests.js --server http://localhost:3000
//
// Requires the Tracy server running (npm start) with a valid ANTHROPIC_API_KEY.

import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import path from "path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const args = process.argv.slice(2);
function flag(name, fallback) {
  const i = args.indexOf(`--${name}`);
  return i !== -1 && args[i + 1] ? args[i + 1] : fallback;
}
const server = flag("server", "http://localhost:3000").replace(/\/$/, "");

const spec = JSON.parse(
  readFileSync(path.join(__dirname, "personality-tests.json"), "utf-8")
);
const surface = spec.surface || "babyresell";

// Collapse a reply to one line so it fits in a markdown table cell.
function cell(text) {
  return String(text ?? "")
    .replace(/\r?\n+/g, " ")
    .replace(/\|/g, "\\|")
    .trim();
}

async function ask(message) {
  const res = await fetch(`${server}/chat`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      surface,
      userId: "personality-tester",
      messages: [{ role: "user", content: message }],
    }),
  });
  if (!res.ok) {
    let detail = "";
    try {
      detail = (await res.json()).error || "";
    } catch {
      /* ignore */
    }
    throw new Error(`HTTP ${res.status} ${detail}`);
  }
  return res.json();
}

async function main() {
  console.log(`Running ${spec.scenarios.length} scenarios on surface "${surface}" via ${server}\n`);

  const rows = [];
  for (const [i, s] of spec.scenarios.entries()) {
    process.stdout.write(`  [${i + 1}/${spec.scenarios.length}] ${s.label} ... `);
    try {
      const data = await ask(s.message);
      const tools = data.toolsUsed?.length ? data.toolsUsed.join(", ") : "—";
      rows.push({ ...s, reply: data.reply, tools });
      console.log("ok");
    } catch (err) {
      rows.push({ ...s, reply: `[ERROR] ${err.message}`, tools: "—" });
      console.log(`failed (${err.message})`);
    }
  }

  const header =
    `| # | Scenario | Message | Tracy's reply | Tools | What we're looking for |\n` +
    `|---|----------|---------|---------------|-------|------------------------|\n`;
  const body = rows
    .map(
      (r, i) =>
        `| ${i + 1} | ${cell(r.label)} | ${cell(r.message)} | ${cell(r.reply)} | ${cell(
          r.tools
        )} | ${cell(r.looking_for)} |`
    )
    .join("\n");

  const table = header + body + "\n";
  const doc =
    `# Personality Test Results\n\n` +
    `Generated by \`scripts/run-personality-tests.js\` against surface \`${surface}\`.\n` +
    `Model and behavior depend on the running server's \`TRACY_MODEL\`.\n\n` +
    table;

  const outPath = path.join(ROOT, "personality-test-results.md");
  writeFileSync(outPath, doc);
  console.log(`\nWrote ${outPath}\n`);
  console.log(table);
}

main().catch((err) => {
  console.error(`\nCouldn't run tests: ${err.message}`);
  console.error(`Is the server running (npm start) with a valid ANTHROPIC_API_KEY?`);
  process.exit(1);
});
